                        <p>บลจ.กรุงศรี เสนอขายกองทุนเปิดกรุงศรีโกลบอลเทคโนโลยีอิควิตี้ 
(KF-GTECH)  ชูศักยภาพในการสร้างผลตอบแทนจากการลงทุน
ในอุตสาหกรรมเทคโนโลยีที่มีปัจจัยพื้นฐานแข็งแกร่ง  ทั้งนี้ กองทุน
อยู่ในระหว่างการพิจารณาคำขออนุมัติจัดตั้งและจัดการกองทุนรวม
จากสำนักงานคณะกรรมการกำกับหลักทรัพย์และตลาดหลักทรัพย์ 
(ก.ล.ต.)  คาดว่าจะสามารถเปิดเสนอขายครั้งแรก
ระหว่างวันที่ 6 – 15 กุมภาพันธ์ 2560 เงินลงทุนขั้นต่ำ 2,000 บาท</p>
                        <p><b>นางสาวศิริพร  สินาเจริญ กรรมการผู้จัดการ บลจ.กรุงศรี</b>  เปิดเผยว่า 
การลงทุนในกลุ่มอุตสาหกรรมเทคโนโลยีเป็นอีกหนึ่งทางเลือกที่น่าสนใจมาก  
และเป็นกลุ่มที่สามารถเติบโตได้ดีท่ามกลางการชะลอตัวของเศรษฐกิจโลก 
เนื่องจากปัจจุบันเทคโนโลยีได้ทวีความสำคัญเพิ่มมากขึ้นในทุกด้าน  กลาย
เป็นสิ่งที่ขาดไม่ได้ในชีวิตประจำวัน  มีการใช้เทคโนโลยีในรูปแบบที่หลาก
หลายทั่วโลก และมีความต้องการใช้งานที่เพิ่มขึ้นอย่างต่อเนื่อง  นอกจากนี้
เรายังเห็นนวัตกรรมทางเทคโนโลยีเกิดขึ้นโดยตลอด  ยิ่งช่วยผลักดัน
ให้อุตสาหกรรมนี้มีแนวโน้มเติบโตได้สูง  ถ้ามองความต้องการทางฝั่ง
องค์กรธุรกิจ  จะเห็นว่าการลงทุนด้าน IT เป็นหนึ่งในต้นทุนที่มี
สัดส่วนสูงที่สุดในหลายองค์กร  ปัจจัยเหล่านี้ส่งผลให้เทคโนโลยีกลาย
เป็นหนึ่งในอุตสาหกรรมที่มีการเติบโตเร็วที่สุดในโลก</p>
                        <p>โดยอุตสาหกรรมเทคโนโลยีมีความน่าสนใจในหลายด้าน ทั้งในส่วนของปัจจัยพื้นฐานที่แข็งแกร่งบนระดับราคาที่สมเหตุสมผล  
ปัจจุบันมูลค่าหุ้นในกลุ่มเทคโนโลยีอยู่ที่ระดับ 10-20 เท่า นับได้ว่าไม่สูงเมื่อเทียบกับอดีต โดยเฉพาะในช่วง dotcom bubble 
ซึ่งมูลค่าหุ้นในกลุ่มดังกล่าวเคยขึ้นไปสูงถึงเกือบระดับ 50 เท่า ในขณะที่ปัจจัยพื้นฐานนั้นได้ปรับตัวดีขึ้นมาก  และมีงบการเงินของ
บริษัทที่แข็งแกร่ง  อัตราการเติบโตของกำไรสุทธิต่อหุ้นสูงที่สุดเมื่อเทียบกับอุตสาหกรรมอื่นๆ 
(แหล่งข้อมูล : T. Rowe Price ณ 31 ธ.ค. 59) ทั้งนี้ บริษัทขนาดใหญ่ที่สุด 5 อันดับแรกของโลกในปี 2016 
เป็นบริษัทในอุตสาหกรรมเทคโนโลยี ได้แก่ Apple , Alphabet , Amazon , Facebook , Microsoft 
(แหล่งข้อมูล : Visual  Capitalist  ณ 12 ส.ค. 59)</p>
                        <p>“บลจ.กรุงศรี เล็งเห็นถึงความน่าสนใจและโอกาสในการรับผลตอบแทนที่ดีจากการลงทุนในกลุ่มอุตสาหกรรมเทคโนโลยี  จึงเปิดเสนอขายกองทุน
เปิดกรุงศรีโกลบอลเทคโนโลยีอิควิตี้ (KF-GTECH)  ที่ลงทุนในกองทุนหลัก T. Rowe Price Funds SICAV – Global Technology Equity 
Fund   ซึ่งบริหารโดยผู้จัดการกองทุนชั้นนำที่มีความเชี่ยวชาญและมีประสบการณ์มากกว่า 16 ปี ในกลุ่มอุตสาหกรรมเทคโนโลยี   กองทุนหลัก
ยังมีจุดเด่นในเรื่องของการบริหารกองทุนเชิงรุก  เน้นลงทุนในบริษัทที่มีนวัตกรรมใหม่ๆในการพลิกโฉมอุตสาหกรรม มีแนวโน้มการเติบโตอย่างรวดเร็ว
ในโลกยุคอินเตอร์เน็ต  มีทรัพย์สินทางปัญญาที่แข็งแกร่ง มีกลุ่มลูกค้าขนาดใหญ่  มีปัจจัยพื้นฐานที่เติบโตอย่างแข็งแกร่ง  และผู้เล่นรายใหม่
เข้าสู่ตลาดได้ยาก ซึ่งเป็นคุณสมบัติที่จะทำให้บริษัทสามารถเติบโตและมีผลกำไรที่ดีในอนาคต”</p>
                        <p>นางสาวศิริพร กล่าวว่า กองทุนหลักจะให้น้ำหนักการลงทุนในบริษัทที่มีนวัตกรรมที่ดีบนระดับราคาที่น่าสนใจ  และเพิ่มน้ำหนักการลงทุนในบริษัท
ขนาดเล็ก หรือบริษัทใหม่ๆที่มีกลยุทธ์การตลาดที่มุ่งเน้นประสิทธิภาพในการผลิต   ทั้งนี้ หลักทรัพย์ที่ลงทุนจะเน้นลงทุนในเทคโนโลยีประเภท 
Artificial intelligence (AI) , Machine learning และ Big data เช่น Salesforce.com , Alibaba , Tesla Motors , 
Amazon.com , Netflix , PayPal Holdings</p>
                        <p>“บลจ.กรุงศรี เชื่อมั่นว่ากองทุน KF-GTECH จะเป็นทางเลือกใหม่ที่น่าสนใจสำหรับนักลงทุนที่กำลังมองหารูปแบบการลงทุนใหม่ๆ  โดยสามารถรับ
ความผันผวนในระยะสั้นและสามารถรับความเสี่ยงในระดับที่สูง  เพื่อโอกาสได้รับผลตอบแทนที่ดีในระยะปานกลางถึงระยะยาว  และควรมีระยะเวลา
ในการถือครองขั้นต่ำตั้งแต่ 1 ปีขึ้นไป  ทั้งนี้  บริษัทได้จัดเตรียมโปรโมชั่นพิเศษสำหรับผู้ที่ลงทุนในกองทุน KF-GTECH ในช่วงการเสนอขายครั้งแรก
ครบทุกๆ100,000บาท จะได้รับหน่วยลงทุนกองทุน KF-GTECH มูลค่า100 บาทด้วย”</p>
                        <p>ทั้งนี้ กองทุน KF-GTECH ลงทุนในกองทุนหลัก T. Rowe Price Funds SICAV – Global Technology Equity Fund โดยเฉลี่ยในรอบปี
บัญชีไม่น้อยกว่าร้อยละ 80 ของNAV  ความเสี่ยงระดับ 7 : เสี่ยงสูง  (ผู้ลงทุนควรขอคำแนะนำเพิ่มเติมก่อนทำการลงทุน) และมีการป้องกันความ
เสี่ยงด้านอัตราแลกเปลี่ยนโดยการตัดสินใจของผู้จัดการกองทุนเพื่อให้เกิดประโยชน์สูงสุดต่อผู้ถือหน่วยลงทุน</p>
